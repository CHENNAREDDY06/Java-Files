package com.javaArrays;

public class Pattern1 {
	public static void main(String[] args) {
		p1();
		p2();
	}
	
public static void p1(){
			
		
		for(int i=0;i<4;i++) {
			System.out.println();
			for(int j=0;j<4;j++) {
				System.out.print("*");
			}
		  }
		}
public static void p2() {
	for(int i=0; i<5;i++) {
		System.out.println();
		for(int j=0; j<=i;j++) {
			System.out.print("*");
		}
		
		
	}

}
		
}
